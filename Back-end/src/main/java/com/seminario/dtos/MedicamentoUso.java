package com.seminario.dtos;

public class MedicamentoUso {

	private String name;
	private Long mes1;
	private Long mes2;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getMes1() {
		return mes1;
	}
	public void setMes1(Long mes1) {
		this.mes1 = mes1;
	}
	public Long getMes2() {
		return mes2;
	}
	public void setMes2(Long mes2) {
		this.mes2 = mes2;
	}
}
